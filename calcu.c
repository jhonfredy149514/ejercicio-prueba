#include"calcu.h"
int suma(int parametro1, int parametro2)
{
return parametro1+parametro2;
}
int resta(int parametro1, int parametro2)
{
return parametro1-parametro2;
}
int multiplicacion(int parametro1, int parametro2)
{
return parametro1*parametro2;
}
int division(int parametro1, int parametro2)
{
return parametro1/parametro2;
}